void setup_tissue( void )
{
	Custom_cell* pC;
	std::vector<init_record> cells = read_init_file(parameters.strings("init_cells_filename"), ';', true);
	
	for (int i = 0; i < cells.size(); i++)
	{
		float x = cells[i].x;
		float y = cells[i].y;
		float z = cells[i].z;
		float radius = cells[i].radius;
		int type = cells[i].type;
		int phase = cells[i].phase;
		double elapsed_time = cells[i].elapsed_time;
		
		if (type == -1) {
			pC = static_cast<Custom_cell*>(create_cell(get_cell_definition("sphere")));
		} else if (type == 0) {
			pC = static_cast<Custom_cell*>(create_cell(get_cell_definition("default")));
		} else {
			pC = static_cast<Custom_cell*>(create_cell(get_cell_definition("other")));	
		}	
		
		pC->assign_position( x, y, z );
		// pC->set_initial_volume(cell_defaults, radius);
	}

	return; 
}

